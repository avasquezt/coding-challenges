function sc(watermelon){
    let cont = [[0,0] , [0,0]];
    for (let i = 0; i < 8; i++) {
       for(let j = 0; j < 8; j++){
        cont[Math.floor(i/4)][Math.floor(j/4)] += watermelon[i][j];
       } 
    }
    return cont.reduce((acc, n) => acc += n.reduce((c, m) => c += m > 5 ? m : 0, 0), 0);
}

